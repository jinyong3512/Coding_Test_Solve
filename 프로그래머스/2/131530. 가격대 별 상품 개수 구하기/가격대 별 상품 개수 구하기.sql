SELECT
    IF(
        FLOOR(PRICE/10000) = 0,
        0,
        CONCAT(FLOOR(PRICE/10000),'0000')
    ) AS PRICE_GROUP,
    COUNT(PRODUCT_ID) AS PRODUCTS
FROM
    PRODUCT
GROUP BY
    FLOOR(PRICE/10000)
ORDER BY
    PRICE_GROUP